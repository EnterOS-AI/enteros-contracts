"""molecule_ai_contracts — installable distribution of the molecule-contracts
generated Python bindings (the contract SSOT, RFC molecule-core#3285).

Import name: ``molecule_ai_contracts``

Re-exports:
  * the MCP plugin-delivery scalar string constants (``contract_gen``)
  * the workspace-comms message ``TypedDict`` models + ``const``-pinned
    string literals (``workspace_comms_gen``)

so consumers (molecule-ai-sdk, molecule_runtime, ...) can::

    from molecule_ai_contracts import REQUIRED_TOOL, RegisterRequest
    from molecule_ai_contracts.contract_gen import MCP_SERVER_NAME
    from molecule_ai_contracts.workspace_comms_gen import HeartbeatRequest

NOTE: this ``__init__.py`` is the package shim — hand-written, NOT generated.
The sibling ``contract_gen.py`` / ``workspace_comms_gen.py`` ARE generated by
``tools/gen-python.mjs`` and must never be hand-edited (the codegen-drift gate
re-runs the generators and fails on any drift under ``gen/``). The generators
only ever (over)write those two files, so this shim is safe from clobbering.

Re-export is by each submodule's declared ``__all__``, filtered to names the
module actually defines: ``workspace_comms_gen.__all__`` re-lists the mcp
scalar names (a generator artifact) that live only in ``contract_gen``, so the
filter keeps the package namespace clean and avoids an import-time error.
"""

from . import contract_gen, workspace_comms_gen  # noqa: F401

__all__: list[str] = []

for _mod in (contract_gen, workspace_comms_gen):
    for _name in getattr(_mod, "__all__", ()):
        if hasattr(_mod, _name) and _name not in __all__:
            globals()[_name] = getattr(_mod, _name)
            __all__.append(_name)

del _mod, _name
